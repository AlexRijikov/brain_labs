public class PostOffice {

    public  String namePost;

    public char geolocation;

    PostOffice(String namePost) {
        this.namePost = namePost;
        this.geolocation = geolocation;

    }

    public String getNamePost() {
        return namePost;
    }

    public char getGeolocation() {
        return geolocation;
    }

    public void setGeolocation(char geolocation) {
        this.geolocation = geolocation;
    }

    public void setNamePost(char geolocation) {
        this.geolocation = geolocation;
    }


}
